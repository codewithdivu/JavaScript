let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);  // remainder
console.log(x ** y);  // y is the power of x (Exponential)

// Increment (++)

console.log(x++)
console.log(++x)

// Decrement (--)

console.log(x--)
console.log(--x)